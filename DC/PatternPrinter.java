public class PatternPrinter {
    public static void main(String[] args) {
        int n = 5; // Change this value to print a different number of elements
        for (int i = 1; i <= n; i++) {
            int result = i * i + 1;
            System.out.print(result + " ");
        }
    }
}
// output pattern 2 5 10 17 26 37 50 .....
